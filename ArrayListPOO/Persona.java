package ArrayListPOO;

class Persona {
    int id, edad;
    String nombre, profesion;    
    Persona() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getProfesion() {
        return profesion;
    }

    public void setProfesion(String profesion) {
        this.profesion = profesion;
    }

    @Override
    public String toString() {
        return "Persona [id=" + id + ", edad=" + edad + ", nombre=" + nombre + ", profesion=" + profesion + "]";
    }

    
}